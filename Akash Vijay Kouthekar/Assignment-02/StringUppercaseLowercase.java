// Program 03 : Convert the given String to uppercase & lowercase  

package assignment02;
public class StringUppercaseLowercase {
	public static void main(String[] args) {
		String string = "Learning Java Is So Much Fun";
		System.out.println("Original String : "+string);
		System.out.println("String to Uppercase : "+string.toUpperCase());
		System.out.println("String to Lowercase : "+string.toLowerCase());
	}
}
